class Bitwise_operator
{
	public static void main(String[] args)
	{
	    int a=492;
	    int b=333;
	    int c=a&b;
	    int d=a|b;
	    int e=a^b;
	    int f=~a;
	    
	    System.out.println("Bitwise AND : " + c);
	    System.out.println("Bitwise OR : " + d);
	    System.out.println("Bitwise EX-OR : " + e);
	    System.out.println("Bitwise NOT: " + f);
	    
	    
	
	}
}